from netzob.Common.Utils.Decorators import NetzobLogger
from netzob.Common.Utils.Decorators import typeCheck


@NetzobLogger
class StateVariable(object):
    """Definition of a state variable. A state variable stores the current value of its associated field in the memory.
    Indeed, the memory is made of state variables, each being associated with a field.    


    """

    def __init__(self):
        """Constructor of a state variable"""
        # create logger with the given configuration

        self.currentValue = None

    @property
    def currentValue(self):
        """Denotes the current value stored in the state variable."""
        return self.__currentValue

    @currentValue.setter
    def currentValue(self, cvalue):
        """Redefines the current value stored in the state variable."""
        self.__currentValue = cvalue
